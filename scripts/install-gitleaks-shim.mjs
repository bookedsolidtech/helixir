#!/usr/bin/env node
/**
 * Postinstall: create a `gitleaks` shim in node_modules/.bin/ that delegates
 * to the bundled `gitleaks-secret-scanner` wrapper.
 *
 * Husky 9 prepends node_modules/.bin to PATH when running git hooks, so the
 * existing `.husky/pre-commit` (which calls `gitleaks` directly) finds the
 * shim on a fresh clone — no separate gitleaks install required.
 *
 * If a real `gitleaks` binary is already on PATH (Homebrew, Linux package,
 * manual install), the shim is still created; the hook's `command -v
 * gitleaks` will find one of them. Both paths produce identical output for
 * `gitleaks protect --staged` because the wrapper proxies to the same
 * gitleaks binary it auto-downloads on install.
 */
import { existsSync, mkdirSync, writeFileSync, chmodSync } from 'node:fs';
import { resolve } from 'node:path';

const binDir = resolve('node_modules', '.bin');
if (!existsSync(binDir)) {
  // pnpm/npm install hasn't created the .bin directory yet — nothing to do.
  process.exit(0);
}

// Don't create a shim until the wrapper itself is installed.
const wrapperPath = resolve(binDir, 'gitleaks-secret-scanner');
if (!existsSync(wrapperPath)) {
  process.exit(0);
}

const shimPath = resolve(binDir, 'gitleaks');

// Don't clobber a real gitleaks binary if one is already symlinked here
// (e.g. from a different package or manual setup).
if (existsSync(shimPath)) {
  process.exit(0);
}

const shimBody = `#!/bin/sh
# Auto-generated by helixir's postinstall (scripts/install-gitleaks-shim.mjs).
# Delegates to the bundled gitleaks-secret-scanner wrapper so .husky/pre-commit
# works on fresh clones without a separate gitleaks install.
exec "$(dirname "$0")/gitleaks-secret-scanner" "$@"
`;

writeFileSync(shimPath, shimBody);
chmodSync(shimPath, 0o755);
process.stdout.write('[helixir] gitleaks shim created in node_modules/.bin\n');
